/*Desarrollar un algoritmo que declare dos variables enteras, le
asigne valores arbitrarios y luego muestre su suma, diferencia y
producto. */


package ejercicios;

public class Practica_Tres {
    public static void main(String[] args) {
        int a = 4;
        int b = 9;

        int suma = a + b;
        int diferencia = a - b;
        int producto = a * b;

        System.out.println("La suma es: " + suma);
        System.out.println("La diferencia es: " + diferencia);
        System.out.println("El producto es: " + producto);
    }
}
